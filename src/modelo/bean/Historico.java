package modelo.bean;


public class Historico {
    
    private int idhistorico;
    private String vendahist;
    private String valorhist;
    private String vendedorhist;

    public int getIdhistorico() {
        return idhistorico;
    }

    public void setIdhistorico(int idhistorico) {
        this.idhistorico = idhistorico;
    }

    public String getVendahist() {
        return vendahist;
    }

    public void setVendahist(String vendahist) {
        this.vendahist = vendahist;
    }

    public String getValorhist() {
        return valorhist;
    }

    public void setValorhist(String valorhist) {
        this.valorhist = valorhist;
    }

    public String getVendedorhist() {
        return vendedorhist;
    }

    public void setVendedorhist(String vendedorhist) {
        this.vendedorhist = vendedorhist;
    }
    
    
    
}
